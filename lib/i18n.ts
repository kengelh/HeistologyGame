const getInitialLanguage = (): Language => {
    const saved = localStorage.getItem('language') as Language;
    if (saved) return saved;

    const browserLang = navigator.language.split('-')[0];
    return browserLang === 'de' ? 'de' : 'en';
};

let currentLanguage: Language = getInitialLanguage();

export const setLanguage = (lang: Language) => {
    currentLanguage = lang;
    localStorage.setItem('language', lang);
    window.dispatchEvent(new Event('languageChanged'));
};

export const getLanguage = (): Language => currentLanguage;

export const t = (key: string, params?: Record<string, any>): string => {
    let current: any = translations[currentLanguage];

    // First, try direct lookup (common for flat keys like 'ui.start_heist')
    if (current[key] !== undefined) {
        current = current[key];
    } else {
        // Fallback: try to resolve path greedily
        const parts = key.split('.');
        let i = 0;
        while (i < parts.length) {
            let found = false;
            for (let j = parts.length; j > i; j--) {
                const subkey = parts.slice(i, j).join('.');
                if (current && current[subkey] !== undefined) {
                    current = current[subkey];
                    i = j;
                    found = true;
                    break;
                }
            }
            if (!found) {
                console.warn(`Translation key not found: ${key}`);
                return key;
            }
        }
    }

    let result = current;
    if (typeof result === 'string' && params) {
        Object.keys(params).forEach(p => {
            result = result.replace(`{${p}}`, String(params[p]));
        });
    }

    return result;
};

export const useTranslation = () => {
    const [lang, setLang] = useState<Language>(currentLanguage);

    useEffect(() => {
        const handleLanguageChange = () => {
            setLang(currentLanguage);
        };
        window.addEventListener('languageChanged', handleLanguageChange);
        return () => window.removeEventListener('languageChanged', handleLanguageChange);
    }, []);

    return { t, lang, setLanguage };
};
